const keys = [];
const locks = [];


maps.forEach(map => {
  if(map[0][0] === '#') {
    locks.push(map);
  }else{
    keys.push(map)
  }
});

console.log('locks: ', locks);
console.log('keys: ', keys);


const getCounts = (list) => {
  const ret = [];
  list.forEach(map => {
    let values = [];
    for(let x = 0; x < map[0].length; x++) {
      let count = 0;

      for(let y = 1; y < map.length - 1; y++) {
        if(map[y][x] === '#'){
          count++;
        }
      }
      values.push(count);
    }
    ret.push(values);
  });
  return ret;
}


const keyHeights = getCounts(keys);
const lockHeights = getCounts(locks);

console.log("keyHeights: ", keyHeights)
console.log("lockHeights: ", lockHeights)

const pairs = [];
keyHeights.forEach(key => {
  lockHeights.forEach(lock => {
    let ok = true;
    for(let i = 0; i < 5; i++) {
      if(key[i] + lock[i] > 5) {
        ok = false;
      }
    }
    if(ok) {
      pairs.push({lock, key});
    }
  })
})

